package br.com.saulocn.hermes.mailer.model;

public class Message {
    private String title;
    private String text;

    public String getTitle() {
        return this.title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getText() {
        return this.text;
    }

    public void setText(String text) {
        this.text = text;
    }
    

}
